package Demo01;

public class test04 {
  public static void main(String[] args) {
    /*
    *   & 与
    *   \ 或
    *   ^ 异或
    *   !取反
    *
    * */
    //与
    System.out.println(true&true);
    System.out.println(true&false);
    //或
    System.out.println(true|true);
    System.out.println(true|false);
    //异或
    System.out.println(true^true);
    System.out.println(true^false);
    //取反
    System.out.println(!true);
    System.out.println(!false);
  }
}
