package day09.work05;

public abstract class Animal {
  private String name;
  private int weight;

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public int getWeight() {
    return weight;
  }

  public void setWeight(int weight) {
    this.weight = weight;
  }

  public abstract void eat();
}
